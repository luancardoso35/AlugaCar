    /**
     * Método que busca os carros na lista de carros que contém o Renavam igual ao informado no
     * campo de texto, e os insere na lista "listaRenavam"
     * @param evt ato de selecionar o botão "Buscar"
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (carregada == false) {
            for (int i = 0; i < ListaCarros.listaCarros.size(); i++)
            {
                if (jTextFieldRenavam.getText().equals(ListaCarros.listaCarros.get(i).getRenavam()))
                {
                    listaRenavam.add(ListaCarros.listaCarros.get(i));
                }
            }
            if (listaRenavam.size() > 0) {
            carregarTabela();
            carregada = true;
            } else {
                JOptionPane.showMessageDialog(this, "Carro não encontrado.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

