    /**
     * Método que remove o carro selecionado na tabela da lista de carros
     * @param evt ato de selecionar o botão "Remover"
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linhaSelecionada = tabelaBusca.getSelectedRow();
        if (tabelaBusca.isRowSelected(linhaSelecionada))
        {
            for (int i = 0; i < ListaCarros.listaCarros.size(); i++)
            {
                if (jTextFieldRenavam.getText().equals(ListaCarros.listaCarros.get(i).getRenavam()))
                {
                    ListaCarros.listaCarros.remove(ListaCarros.listaCarros.get(i));
                    listaRenavam.clear();
                    jTextFieldRenavam.setText("");
                    recarregarTabela();
                    JOptionPane.showMessageDialog(this, "Carro removido com sucesso!");
                    carregada = false;
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

