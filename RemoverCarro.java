    /**
     * Método que carrega a tabela com todos os carros da lista "listaRenavam", que
     * contém os carros que possuem Renavam igual ao informado no campo de texto
     */
    private void carregarTabela()
    {
        DefaultTableModel localModel = (DefaultTableModel) tabelaBusca.getModel();
        
        for (Carro C : this.listaRenavam)
        {
            Object[] row = new Object[4];
            row[0] = String.valueOf(C.getMarca());
            row[1] = String.valueOf(C.getModelo());
            row[2] = String.valueOf(C.getCor());
            row[3] = String.valueOf(C.getRenavam());
            localModel.addRow(row);
        }
    }
    
    /**
     * Método que esvazia a tabela apresentada
     */
    private void recarregarTabela()
    {
        DefaultTableModel localModel = (DefaultTableModel) tabelaBusca.getModel();
        localModel.setRowCount(0);
    }
    
